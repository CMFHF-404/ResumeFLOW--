JD_ANALYSIS_SHARED_RUBRIC = (
    "Scoring principles for experiences: "
    "1) First restore the candidate's actual behavior from the text before scoring. "
    "Distinguish between owning, leading, independently executing, collaborating in support, "
    "following up on tasks, relaying information, and organizing materials or process notes. "
    "2) Then judge responsibility level: whether the candidate is accountable for goals, plans, "
    "execution quality, delivery, decisions, or results, versus mainly assisting or coordinating. "
    "3) Then judge scale and scope: team size, business scale, user or customer reach, event size, "
    "cross-team complexity, system criticality, and operational breadth. "
    "4) Then judge value and impact: revenue, growth, cost, efficiency, delivery, quality, risk control, "
    "process improvement, or other concrete business outcomes. "
    "5) Finally judge alignment with the JD requirements using the evidence above. "
    "Do not reward inflated wording by itself. For vague, polished, or abstract statements, first infer the "
    "most likely real work content conservatively from the provided evidence, and do not assume ownership, "
    "decision authority, or measurable impact unless the text supports it. High-level verbs alone are not enough; "
    "look for object, method, scope, and result. For supportive, collaborative, or administrative wording, keep a "
    "neutral interpretation and do not automatically upgrade it to owner-level work. "
    "If evidence is insufficient, score conservatively instead of imagining stronger responsibility. "
    "Scale and value MUST affect both experience-level scores and the overall matchPercentage. "
    "Under similar work depth, larger scale or higher business value can raise the score. "
    "Employer brand or platform scale may be used only as one scale signal, never as the sole reason for a high score. "
    "A smaller company experience with stronger ownership or clearer outcomes may score higher than a larger company "
    "experience with mainly supportive execution. "
    "When writing experience reasons, explain the score mainly through actual behavior, responsibility level, scale, "
    "value, and JD relevance. Avoid generic praise."
)

